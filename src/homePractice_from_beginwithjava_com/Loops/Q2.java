package homePractice_from_beginwithjava_com.Loops;

public class Q2 {
    public static void main(String[] args) {

        //Question 2
       // Write a program to calculate the sum of first 10 natural number.

        int count = 0;
        for ( int i = 1 ; i <= 10 ; i++) {
            count += i;
        }
        System.out.println(count);






    }
}
